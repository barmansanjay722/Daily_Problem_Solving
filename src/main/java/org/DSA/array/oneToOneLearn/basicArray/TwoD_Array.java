package org.DSA.array.oneToOneLearn.basicArray;

public class TwoD_Array {
    public static void main(String[] args) {

        int marks[][] = {
                {12,98,34},
                {12,67,87},
                {12,98,23}
        };

        System.out.println(marks[2][1]);
        System.out.println(marks[2][2]);
        System.out.println(marks[1][2]);
    }
}
