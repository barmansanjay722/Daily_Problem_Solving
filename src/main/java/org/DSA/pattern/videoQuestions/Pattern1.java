package org.DSA.pattern.videoQuestions;

public class Pattern1 {
    public static void main(String[] args) {
        int n = 5;

        for (int i = 0; i <n; i++) {
            for(int j=0;j<=7;j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
